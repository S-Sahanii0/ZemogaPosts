import 'package:flutter/widgets.dart';

/// Defines the color palette for the App.
abstract class CustomColor {
  /// Black 25% opacity
  static const Color black25 = Color(0x40202124);

  /// Black 25% opacity
  static const Color black = Color(0xFF000000);

  /// Gray
  static const Color gray = Color(0xFFCFCFCF);

  /// White
  static const Color white = Color(0xFFFFFFFF);

  /// WhiteBackground
  static const Color whiteBackground = Color(0xFFE8EAED);

  /// Transparent
  static const Color transparent = Color(0x00000000);

  /// Dark Red
  static const Color darkRed = Color(0xFFBB271A);

  /// Dark Green
  static const Color darkGreen = Color(0xFF218559);

  /// Blue 90
  static const Color blue90 = Color.fromARGB(255, 5, 8, 11);

  /// Blue 50
  static const Color blue50 = Color(0xFF027DFD);
}
